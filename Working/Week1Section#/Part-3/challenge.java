public class challenge {

    public static void main(String[] args) {

        int pound = 200;
        double kilogram = (double) (0.45359237 * pound);
        System.out.println("Converted to kilogram: " + kilogram);


        double p = 200d;
        double k = (0.45359237 * p);
        System.out.println("Converted to kilogram: " + k);
    }

    
}